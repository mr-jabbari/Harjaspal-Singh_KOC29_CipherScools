import os
import time

acronym_list = []

def acronym(string):
    acronym = string[0]
    for i in range(1, len(string)):
        if(string[i-1] == ' '):
            acronym += string[i]
    return acronym.upper()

while(True):
    os.system('cls')
    choice = int(input("""
    |-----------------------------------------------------------------|
    |  1. Make another Acronym                                        |
    |-----------------------------------------------------------------|
    |  0. Quit                                                        |
    |-----------------------------------------------------------------|\n\n"""))
    time.sleep(0.5)
    os.system('cls')
    if(choice == 1):
        string = str(input("""
        |-----------------------------------------------------------------|
        |           Enter Your Desired String                             |
        |-----------------------------------------------------------------|\n\n"""))
        acronym_list.append(acronym(string))

    elif(choice == 0):
        break

    else:
        print("""
        |-----------------------------------------------------------------|
        |                        Invalid Choice!!!!!!!!!                  |
        |-----------------------------------------------------------------|""")
        time.sleep(2)
        os.system('cls')
        print("""
        |-----------------------------------------------------------------|
        |                        Restarting.............                  |
        |-----------------------------------------------------------------|""")
        time.sleep(3)
        os.system('cls')

for i in list(acronym_list):
     print("""
        |-----------------------------------------------------------------|
        |                        """, i, """                                    |
        |-----------------------------------------------------------------|""", end='')
